
public class Class26 {

	static int count = 2;
	
	public static void main(String[] args) {
		int number = 9;
		
		for (int i=0; i<10; i++) {
			count++;
		}
	
		// while loop: based on the condition 
		//if conditionis true then the loop will iterate,
		//if condition is false then loop will terminate		
		
		while (count < number) {
			System.out.println(count);
			
		
		}
		

	}

}
